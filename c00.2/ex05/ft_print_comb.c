#include <unistd.h>

void	condicion(char n)
{
	if (n != '7')
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	char	a[3];

	a[0] = '0';
	a[1] = '1';
	a[2] = '2';
	while (a[0] <= '9')
	{
		while (a[1] <= '9')
		{
			while (a[2] <= '9')
			{
				write(1, &a[0], 1);
				write(1, &a[1], 1);
				write(1, &a[2], 1);
				condicion(a[0]);
				a[2]++;
			}
			a[1]++;
			a[2] = a[1] +1;
		}
		a[1] = a[0] +1;
		a[0]++;
	}
}

int	main(void)
{
	ft_print_comb();
}
